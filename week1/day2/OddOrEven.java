package week1.day2;


public class OddOrEven {

	public static void main(String[] args) {
	
	
		
		for (int i=1; i<=10; i++)

		{
		if(i%2==0)
		{
            System.out.println("The number is even "+ i);
            }
        else
        {
            System.out.println("The number is odd "+ i);
   
		
	
		}
	
}
}
}

